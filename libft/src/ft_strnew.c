#include "../includes/libft.h"

char	*ft_strnew(size_t size)
{
	char	*arr;

	if (size > size + 1)
		return (NULL);
	if (!(arr = (char *)malloc(size + 1 * sizeof(char))))
		return (NULL);
	ft_bzero(arr, size);
	arr[size] = '\0';
	return (arr);
}
