#include "../includes/libft.h"

int					ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	size_t			j;
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	j = 0;
	while (j < n)
	{
		if (str1[i] == str2[i])
			i++;
		else if (str1[i] != str2[i])
			return (str1[i] - str2[i]);
		j++;
	}
	return (0);
}
